export function alugarFilme(titulo, tempo)
{
    return `O cliente alugou o filme ${titulo} por: ${tempo} dias`
}
    export function precoFilme(titulo, preco)
{
    return `O cliente alugou o filme ${titulo} por: R$ ${preco}`
}
    export function avaliarFilme(titulo, nota)
{
    return `O cliente avaliou o filme ${titulo} com: ${nota} estrelas`
}